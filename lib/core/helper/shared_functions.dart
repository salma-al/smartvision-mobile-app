String generateDateString(DateTime date) {
  return '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
}

String generateDayMonth(DateTime date) {
  return '${getMonthName(date.month)} ${date.day}';
}

String generateFullDate(DateTime date) {
  return '${getMonthName(date.month)} ${date.day}, ${date.year}';
}